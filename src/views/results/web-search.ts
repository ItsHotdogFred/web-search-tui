import { TextRenderable } from "@opentui/core";
import OpenAI from "openai";
import type { HandlerContext } from "./types";
import { LAYOUT } from "./types";
import {
  renderSearchQuery,
  renderResultsMeta,
  renderDivider,
  renderError,
  renderResultScrollbox,
  createResultBlock,
  getWrappedContentHeight,
  textPrimary,
} from "./helpers";
import { createMarkdown } from "../../components/markdown";

const MODEL = "x-ai/grok-4.20";
const ENABLE_AI_SUMMARY = false;

export async function handleWebSearch(ctx: HandlerContext, query: string): Promise<void> {
  ctx.searchInput.placeholder = "Searching...";

  let result;
  try {
    result = await ctx.exa.search(query, {
      type: "fast",
      contents: false,
    });
  } catch {
    ctx.renderer.root.remove(ctx.searchInputId);
    renderSearchQuery(ctx.renderer, ctx.aiSummary.query);
    renderError(ctx.renderer, ctx.resultTextId, "Search failed. Please try again.", LAYOUT.CONTENT_START_Y);
    return;
  }

  ctx.renderer.root.remove(ctx.searchInputId);
  renderSearchQuery(ctx.renderer, ctx.aiSummary.query);
  ctx.aiSummary.content = "";
  ctx.aiSummary.height = 0;

  if (ENABLE_AI_SUMMARY) {
    await generateAiSummary(ctx, result, query);
  }

  const results = result.results ?? [];
  renderResultsMeta(ctx.renderer, `${results.length} web results`, LAYOUT.META_Y);
  renderDivider(ctx.renderer, LAYOUT.DIVIDER_Y);

  const scrollboxY = ENABLE_AI_SUMMARY && ctx.aiSummary.content
    ? 7 + Number(ctx.aiSummary.height) + 1
    : LAYOUT.CONTENT_START_Y;

  if (results.length === 0) {
    renderError(ctx.renderer, ctx.resultTextId, "No results found.", scrollboxY);
    return;
  }

  const entries = results.map((item, index) => {
    const title = item.title ?? "Untitled";
    const url = item.url ?? "";

    return {
      renderable: new TextRenderable(ctx.renderer, {
        id: `searchResult-${index}`,
        content: createResultBlock(index, title, "", url),
        fg: textPrimary,
      }),
      url: item.url,
    };
  });

  renderResultScrollbox(ctx.renderer, entries, ctx.index, scrollboxY, ctx.searchResults, ctx.searchUrls);
}

async function generateAiSummary(ctx: HandlerContext, result: unknown, query: string): Promise<void> {
  const apiKey = process.env.OPENROUTER_API_KEY;
  if (!apiKey) return;

  const client = new OpenAI({
    apiKey,
    baseURL: "https://openrouter.ai/api/v1",
  });

  try {
    const completion = await client.chat.completions.create({
      model: MODEL,
      messages: [
        {
          role: "system",
          content: "You are an AI which is designed to summarize web searches, ensure summarizes are only around 3 - 5 sentences long and provide the detail needed to satisfy the users request. Also ensure you cite the websites you used for the data.",
        },
        { role: "system", content: "Here is the data from the search request:" + JSON.stringify(result) },
        { role: "user", content: query.trim() },
      ],
    });

    const completionText =
      (completion.choices[0]?.message?.content ?? "No response generated.") +
      "\n Generated by: " +
      MODEL;

    const summaryWidth = Math.max(process.stdout.columns - 4, 20);
    const summaryHeight = getWrappedContentHeight(completionText, summaryWidth) + 1;

    ctx.aiSummary.content = completionText;
    ctx.aiSummary.height = summaryHeight;

    await createMarkdown(ctx.renderer, completionText, "ai-generated", {
      x: 0,
      y: 7,
      width: "100%",
      height: ctx.aiSummary.height,
    });
  } catch {
    // AI summary is optional; silently skip on failure
  }
}
